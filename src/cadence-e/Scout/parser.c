	w.ymax = f.f.ymax;
	break;
    case FONT:
	if (w.font)
	    freetree(w.font);
	w.font = f.f.font;
	break;
    case BG:
	if (w.bgcolor)
	    freetree(w.bgcolor);
	w.bgcolor = f.f.bgcolor;
